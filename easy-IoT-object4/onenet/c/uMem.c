#ifdef ONENET_ENABLE

/*
;*****************************************************************************************************
;* 函数名称 : uMEM_Init
;* 描    述 : 内存管理初始化
;* 输　	 入 : Addr: 有效的内存块的起始地址, Size: 内存块的大小
;*   
;* 输　	 出 : TRUE OR FALSE
;*----------------------------------------------------------------------------------------------------
;* 修改备注 : 
;*****************************************************************************************************
;*/
uint8 uMEM_Init(void)
{
    return 0;
}


/*
;*****************************************************************************************************
;* 函数名称 : uMEM_New
;* 描    述 : 内存申请
;* 输　	 入 : Size: 内存块的大小
;*   
;* 输　	 出 : 可用的内存块的起始地址 或 NULL
;*----------------------------------------------------------------------------------------------------
;* 修改备注 : 
;*****************************************************************************************************
;*/
void *uMEM_New(uint32 Size)
{
    return mem_malloc(Size);
}


/*
;*****************************************************************************************************
;* 函数名称 : uMEM_Free
;* 描    述 : 内存释放
;* 输　	 入 : Addr: 已分配的内存块的起始地址
;*   
;* 输　	 出 : 无
;*----------------------------------------------------------------------------------------------------
;* 修改备注 : 
;*****************************************************************************************************
;*/
void uMEM_Free(void *Addr)
{
    mem_free(Addr);
}
#endif
